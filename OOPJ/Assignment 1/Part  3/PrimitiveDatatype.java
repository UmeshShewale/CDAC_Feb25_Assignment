//Q1. Write a Java program to declare and initialize all eight primitive data types and print their values.
import java.util.*;

class PrimitiveDatatype{
	public static void main(String args[]){
		byte a = 123;
		short b = 3345;
		int c = 23456;
		long d = 345678L;
		float e = 1234.0f;
		double f = 24345.9887345584;
		char ch = 'B';
		boolean num = true;
		
		System.out.println("byte value is : " + a);
		System.out.println("short value is : " + b);
		System.out.println("int value is : " + c);
		System.out.println("long value is : " + d);
		System.out.println("float value is : " + e);
		System.out.println("double value is : " + f);
		System.out.println("char value is : " + ch );
		System.out.println("boolean value is : " + num);
		
		
	}
}